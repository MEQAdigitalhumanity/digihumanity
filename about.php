本計畫做為「數位人文創新人才培育計畫」的先導型計畫，旨在配合教育部之「促進人文與科技跨領域創新合作」、「推動前瞻科技人才培育與學產研鏈結」、「發展數位學習與應用」及「落實能源與校園環境永續」等4項之科技施政目標，利用先期規畫期間擬定四年期(112-115年)「綱要計畫書」，其宏觀目標在於：深入「數位人文學」，融匯數位科技特性與人文社會本質，探索數位人文整合之素養、方法與內涵，透過優質課程與凝聚社群，引導學生以數位科技及量化分析技術強化人文社科知能，培育具有國際視野、豐富涵養，具科技知能競爭力之數位人文創新人才。整體計畫目標架構如下：
</p>
          <blockquote><p><strong>(1)創新素養範式：</strong>「數位人文學」一脈縱深，數位科技特性與人文社會本質交叉融匯。透過計畫執行，憑藉人社領域師生教研的能量，共同探索數位人文整合之素養、方法與內涵，建立其範式，創造人社領域新的詮釋角度與創新研究方法。師生提升觀照的層次，開展獨創的設計。
</p></blockquote>

          <blockquote><p><strong>(2)建置發展環境：</strong>鼓勵大學校院建置數位人文跨域課群與社群機制，營造數位人文發展環境。由系所院等教學單位開設人社領域數位人文系列課群，研發數位人文跨域教學模組；同時以凝聚社群與研習管道，提供研修支持系統，引導學生紮根人文理論，銜接產業實習實務應用管道，以有效強化人社學科之優勢，注入數位科技的新生命。
</p></blockquote>

          <blockquote><p><strong>(3)培育跨域人才：</strong>鼓勵人社領域學生，基於人文底蘊與社會關懷情操，以及批判思考與問題解決等知能，透過課群與知能研習強化學生運用數位科技及量化分析技能，擴充人文本質、加值人文價值，導入數位科技技術，產生創新主題型態與獨創設計，培養成數位人文跨域創新人才。
</p></blockquote>

          <blockquote><p><strong>(4)拔擢教研典範：</strong>彙整課程精選、學習資源、國內外典範方案，提供師生學研參考。各年度遴選推薦優秀教師教研成果與學生學研作品，在全國與國際舞台展示，與其他學術界、產業界、設計界的同好交流想法，擴展視野、提升認同感與成就感，展現其學習成果。相關教研與學習產出，進而滾動擴充、豐富學習資源資料庫。
</p></blockquote>

